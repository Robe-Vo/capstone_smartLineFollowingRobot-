function cfg = makeCfgDefault()

    cfg = struct();

    cfg.proto = capstone.io.Protocol.constants();

    cfg.timing = struct( ...
        "dtControlS", 0.02, ...     % app timer tick (20ms); đổi tùy bạn
        "rxTimeoutS", 0.2, ...
        "idlePauseS", 0.001 );

    cfg.drive = struct( ...
        "baseSpeedU8", uint8(100) );

    cfg.steer = struct( ...
        "centerU16", uint16(86), ...
        "minU16", uint16(55), ...
        "maxU16", uint16(110), ...
        "outMaxDeg", 45.0 );

    white = [  32.450   30.980   30.930   32.520   32.1600];
    black = [ 131.570  163.400  115.840   50.370   56.190];

    cfg.line = struct( ...
        "offset", white, ...
        "scale",  1 ./ (black - white), ...
        "threshold", 0.5*ones(1,5), ...
        "positions", [-2 -2 0 2 2], ...
        "deadbandPos", 0.10 );

    cfg.pidDefault = struct( ...
        "Kp", 50.0, "Ki", 0.0, "Kd", 0.2, ...
        "intMax", 10.0, ...
        "outMaxDeg", 45.0 );
end

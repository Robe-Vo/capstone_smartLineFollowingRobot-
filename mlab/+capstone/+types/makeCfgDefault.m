function cfg = makeCfgDefault()
cfg = struct();

cfg.proto = struct( ...
    "CMD_MODE_OP", uint8(hex2dec('FF')), ...
    "CMD_MODE_IDLE", uint8(hex2dec('FE')), ...
    "CMD_CTRL", uint8(hex2dec('F1')), ...
    "CMD_STOP", uint8(hex2dec('F0')), ...
    "ACK", uint8(hex2dec('20')) );

cfg.timing = struct( ...
    "dtControlS", 0.02, ...   % tick app; servo realtime theo frame
    "rxTimeoutS", 1.0 );

cfg.drive = struct("baseSpeedU8", uint8(100));

cfg.steer = struct( ...
    "centerU16", uint16(86), ...
    "minU16", uint16(55), ...
    "maxU16", uint16(110) );

white = [32.450 30.980 30.930 32.520 32.160];
black = [131.570 163.400 115.840 50.370 56.190];

cfg.line = struct( ...
    "offset", white, ...
    "scale",  1./(black - white), ...
    "threshold", 0.5*ones(1,5), ...
    "positions", [-2 -2 0 2 2], ...
    "deadbandPos", 0.10 );
end
